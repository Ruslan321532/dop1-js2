function minMax(arr){
    return [Math.max(...arr), Math.min(...arr)];
  }
  console.log(minMax([1,2,3,4,5]));

function minMax(arr1){
    return [Math.max(...arr1), Math.min(...arr1)];
  }
  console.log(minMax([1,2,-3,4,5]));

function minMax(arr2){
    return [Math.max(...arr2), Math.min(...arr2)];
  }
  console.log(minMax([1,9,3,4,-5]));